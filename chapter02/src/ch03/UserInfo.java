package ch03;

public class UserInfo {

	String userId;
	String userPassWrod;
	String userName;
    String userAddress;
	String phoneNumber;
	int age;
	
	public UserInfo(String userId, String userPassWrod, String userName, String userAddress, String phoneNumber,
			int age) {
		this.userId = userId;
		this.userPassWrod = userPassWrod;
		this.userName = userName;
		this.userAddress = userAddress;
		this.phoneNumber = phoneNumber;
		this.age = age;
	}
}
	
